    public void save(String path) {
        try {
            File f = new File(path);
            FileWriter fw = new FileWriter(f,true);
            BufferedWriter bw = new BufferedWriter(fw);
            if (path == "svat.txt") {
                for (SinhvienATTT x : svattt) {
                    bw.write(x.toString());
                    bw.newLine();
                }
            } else {
                for (SinhvienMatMa x : svMas) {
                    bw.write(x.toString());
                    bw.newLine();
                }
            }
            bw.close();
            fw.close();
        } catch (Exception e) {
            System.out.println(e);
        }

    }

