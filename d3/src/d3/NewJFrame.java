    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        // TODO add your handling code here:
        
        error = "";
        Date ngaysinh = null;
        double diem = 0, luong = 0, hocphi = 0;
        String masv, hoten, gioitinh, donvi;

        svMas.clear();
        svattt.clear();
        if (msv.getText().trim().isEmpty() || ht.getText().trim().isEmpty() || ns.getText().trim().isEmpty()
                || d.getText().trim().isEmpty() || (!nam.isSelected() && !nu.isSelected())) {
            if (chon.getSelectedItem().toString() == "SVATTT") {
                if (hp.getText().trim().isEmpty()) {
                    error += "Nhap day du thong tin SVATTT";
                }
            } else {
                if (l.getText().trim().isEmpty() || dv.getText().trim().isEmpty()) {
                    error += "Nhap day du thong tin SVMM";
                }
            }
        } else {
            try {
                SimpleDateFormat fm = new SimpleDateFormat("dd/mm/yyyy");
                ngaysinh = fm.parse(ns.getText());
            } catch (Exception e) {
                error += "ngay sinh khong hop le";
            }
            try {
                diem = Double.parseDouble(d.getText());
            } catch (Exception e) {
                error += "nhap dung dinh dang";
            }
            if (chon.getSelectedItem().toString() != "SVATTT") {
                try {
                    luong = Double.parseDouble(l.getText());
                } catch (Exception e) {
                    error += "nhap dung dinh dang";
                }

            }
        }
        if (error == "") {
            if (chon.getSelectedItem().toString() == "SVATTT") {
                svattt.add(new SinhvienATTT(hocphi, msv.getText(), ht.getText(), nam.isSelected() ? "nam" : "nu", ngaysinh, diem));
                save("svat.txt");
            } else {
                svMas.add(new SinhvienMatMa(dv.getText(), luong, msv.getText(), ht.getText(), nam.isSelected() ? "nam" : "nu", ngaysinh, diem));
                save("svmm.txt");
            }
        } else {
            JOptionPane.showMessageDialog(this, error);
        }

    }//GEN-LAST:event_themActionPerformed

